LPDIRECTINPUT8 CInput::m_pInput = NULL;

// コンストラクタ
CInput::CInput()
{

}

// デストラクタ
CInput::~CInput()
{

}

HRESULT CInput::Init(HINSTANCE hInstance, HWND hWnd)
{
	if (m_pInput == NULL)
	{
		// DirectInputオブジェクトの作成
		if (FAILED(DirectInput8Create(hInstance, DIRECTINPUT_VERSION, IID_IDirectInput8, (void**)&m_pInput, NULL)))
		{
			return E_FAIL;
		}
	}

	return S_OK;
}

void CInput::Uninit()
{
	// デバイスの開放
	if (m_pDevice != NULL)
	{
		m_pDevice->Unacquire();	// アクセス権
		m_pDevice->Release();
		m_pDevice = NULL;
	}

	// オブジェクトの開放
	if (m_pInput != NULL)
	{
		m_pInput->Release();
		m_pInput = NULL;
	}
}
