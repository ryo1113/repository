#ifndef _MOUSE_H_
#define _MOUSE_H_

//======================================================================================================================
// マクロ定義
//======================================================================================================================
#define	NUM_KEY_MAX				(256)	// キーの最大数
#define	NUM_BUTTON_MAX			(8)		// ボタン(マウス)の最大数

//======================================================================================================================
// インクルードファイル
//======================================================================================================================
#include "main.h"
#include "input.h"

//======================================================================================================================
// 構造体定義
//======================================================================================================================

//======================================================================================================================
// クラス定義
//======================================================================================================================
class CMouse : public CInput
{
public:
	CMouse();
	~CMouse();

	//======================================================================================================================
	// ボタンの種類
	//======================================================================================================================
	enum MOUSEBUTTON
	{
		MOUSEBUTTON_LEFT,
		MOUSEBUTTON_RIGHT,
		MOUSEBUTTON_WHEELPUSH,
		MOUSEBUTTON_MAX
	};

	HRESULT Init(HINSTANCE hInstance, HWND hWnd);
	void Update();
	void Uninit();

	bool GetMousePress(MOUSEBUTTON nButton);
	bool GetMousePressTrigger(MOUSEBUTTON nButton);

	LONG GetMouseX();
	LONG GetMouseY();

private:

	struct MOUSE
	{
		DIMOUSESTATE2 mouseState;
		D3DXVECTOR2 pos;
	};

	MOUSE					m_mouse;								// マウスの情報

	BYTE					m_aButtonState[NUM_BUTTON_MAX];			// マウスの入力情報ワーク
	BYTE					m_aButtonStateTrigger[NUM_BUTTON_MAX];	// マウスのトリガー

	HWND					m_hWnd;									// ウィンドウハンドル
};

#endif
