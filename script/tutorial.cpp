//======================================================================================================================
// タイトル生成
//======================================================================================================================
CTutorial *CTutorial::Create()
{
	CTutorial *pTitle;

	pTitle = new CTutorial;

	pTitle->Init();

	return pTitle;
}

//======================================================================================================================
// 初期化
//======================================================================================================================
void CTutorial::Init()
{
	CWord::Create(CWord::WORD_TUTORIAL_BG, D3DXVECTOR3(SCREEN_WIDTH / 2.0f, SCREEN_HEIGHT / 2.0f, 0.0f), D3DXVECTOR3(SCREEN_WIDTH, SCREEN_HEIGHT, 0.0f));
	CWord::Create(CWord::WORD_ENTER, D3DXVECTOR3(1030.0f, 670.0f, 0.0f), D3DXVECTOR3(500.0f, 70.0f, 0.0f));
}

//======================================================================================================================
// 開放処理
//======================================================================================================================
void CTutorial::Uninit()
{

}

//======================================================================================================================
// 更新処理
//======================================================================================================================
void CTutorial::Update()
{
	CKeyboard *pKey = CManager::GetInputKeyboard();
	CPad *pPad = CManager::GetInputPad();

	if (pKey->GetKeyboardTrigger(DIK_RETURN) || pPad->GetJoypadTrigger(0, CPad::JOYPADKEY_B))
	{
		CRenderer::SetFade(CManager::MODE_GAME);
	}
}

